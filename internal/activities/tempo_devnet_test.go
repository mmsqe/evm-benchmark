// twoLocalVals / identities are a fixed 2-validator local fixture used to pin
// the generated launcher byte-for-byte against tempo-py's output format.
var (
	twoLocalVals = []tempoDevnetValidator{
		{Moniker: "node0", Host: "127.0.0.1", Port: 8000},
		{Moniker: "node1", Host: "127.0.0.1", Port: 8006},
	}
	fixedIdentities = []string{"aaa", "bbb"}
)

// TestTempoRunScriptLocalFormat pins the local run.sh byte format. tempo-xtask
// emits keys and identities; the launcher wiring is ours, so its exact shape
// (an exec'd command, one single-quoted arg per continued line, trailing
// newline, no `set -eu`) must match what `tempo node` was verified to accept.
func TestTempoRunScriptLocalFormat(t *testing.T) {
	args := tempoNodeArgs(8006, "127.0.0.1", "127.0.0.1", "0.0.0.0",
		tempoLocalTrustedPeers(twoLocalVals, fixedIdentities), false)
	got := tempoRunScript("/opt/tempo", args, false)

	want := `#!/bin/sh
# auto-generated by evm-benchmark

cd "$(dirname "$0")"

exec '/opt/tempo' \
  'node' \
  '--consensus.signing-key' \
  './signing.key' \
  '--consensus.secret' \
  './.secret' \
  '--consensus.signing-share' \
  './signing.share' \
  '--consensus.listen-address' \
  '127.0.0.1:8006' \
  '--consensus.metrics-address' \
  '127.0.0.1:8008' \
  '--chain' \
  './genesis.json' \
  '--datadir' \
  '.' \
  '--port' \
  '8007' \
  '--discovery.port' \
  '8007' \
  '--p2p-secret-key' \
  './enode.key' \
  '--trusted-peers' \
  'enode://aaa@127.0.0.1:8001,enode://bbb@127.0.0.1:8007' \
  '--authrpc.port' \
  '8009' \
  '--http' \
  '--http.addr' \
  '0.0.0.0' \
  '--http.port' \
  '8010' \
  '--http.api' \
  'all' \
  '--ws' \
  '--ws.addr' \
  '0.0.0.0' \
  '--ws.port' \
  '8011' \
  '--consensus.use-local-defaults' \
  '--consensus.allow-private-ips'
`
	if got != want {
		t.Errorf("local run.sh mismatch:\n--- got ---\n%s\n--- want ---\n%s", got, want)
	}
}

