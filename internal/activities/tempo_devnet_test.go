// TestTempoTrustedPeers pins the peer-wiring divergence: local advertises every
// validator (self included) at its host loopback and execution-p2p port; docker
// advertises the OTHER validators by service name at the fixed 8001 port.
func TestTempoTrustedPeers(t *testing.T) {
	local := tempoLocalTrustedPeers(twoLocalVals, fixedIdentities)
	wantLocal := []string{
		"enode://aaa@127.0.0.1:8001",
		"enode://bbb@127.0.0.1:8007",
	}
	if strings.Join(local, ",") != strings.Join(wantLocal, ",") {
		t.Errorf("local peers = %v, want %v", local, wantLocal)
	}

	docker := tempoDockerTrustedPeers(twoLocalVals, fixedIdentities, 1)
	wantDocker := []string{"enode://aaa@node0:8001"}
	if strings.Join(docker, ",") != strings.Join(wantDocker, ",") {
		t.Errorf("docker peers (self=1) = %v, want %v", docker, wantDocker)
	}
}

// TestTempoDockerIP pins the static-IP scheme baked into genesis and compose:
// host octet base 10, one per validator, on the default subnet.
func TestTempoDockerIP(t *testing.T) {
	for idx, want := range map[int]string{0: "10.88.0.10", 1: "10.88.0.11", 3: "10.88.0.13"} {
		got, err := tempoDockerIP(tempoDockerSubnet, idx)
		if err != nil {
			t.Fatalf("index %d: %v", idx, err)
		}
		if got != want {
			t.Errorf("dockerIP(%d) = %q, want %q", idx, got, want)
		}
	}
}

// TestTempoXtaskGenesisArgs guards the defaults that used to live in tempo-py's
// DevnetConfig: an unset epoch/gas must fall back to 100 / 500M rather than 0,
// which tempo-xtask would reject or mis-size the block for.
func TestTempoXtaskGenesisArgs(t *testing.T) {
	base := messages.BenchmarkSpec{
		EVMChainID: 1337, NumAccounts: 8, Validators: 2,
		BaseMnemonic: "test test test test test test test test test test test junk",
	}
	got := strings.Join(tempoXtaskGenesisArgs(base, "127.0.0.1:8000,127.0.0.1:8006"), " ")
	for _, want := range []string{
		"--chain-id 1337",
		"--accounts 17", // 8*2 + 1 validator key
		"--epoch-length 100",
		"--gas-limit 500000000",
		"--validators 127.0.0.1:8000,127.0.0.1:8006",
	} {
		if !strings.Contains(got, want) {
			t.Errorf("genesis args %q missing %q", got, want)
		}
	}

	overridden := base
	overridden.TempoEpochLength = 50
	overridden.TempoGasLimit = 3_000_000_000
	got = strings.Join(tempoXtaskGenesisArgs(overridden, "x"), " ")
	if !strings.Contains(got, "--epoch-length 50") || !strings.Contains(got, "--gas-limit 3000000000") {
		t.Errorf("overrides not honoured: %q", got)
	}
}

// TestDeepMergeJSON guards the genesis-patch semantics: nested objects merge
// recursively while scalars and arrays are replaced wholesale.
func TestDeepMergeJSON(t *testing.T) {
	dst := map[string]interface{}{
		"config": map[string]interface{}{"chainId": float64(1), "keep": "me"},
		"scalar": "old",
	}
	src := map[string]interface{}{
		"config": map[string]interface{}{"chainId": float64(2)},
		"scalar": "new",
	}
	out := deepMergeJSON(dst, src)
	cfg := out["config"].(map[string]interface{})
	if cfg["chainId"] != float64(2) {
		t.Errorf("chainId not overridden: %v", cfg["chainId"])
	}
	if cfg["keep"] != "me" {
		t.Errorf("sibling key dropped during merge: %v", cfg)
	}
	if out["scalar"] != "new" {
		t.Errorf("scalar not replaced: %v", out["scalar"])
	}
}
